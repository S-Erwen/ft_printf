#include "libft.h"

void				*ft_memmove(void *dest, const void *src, size_t n)
{
	unsigned char	*d;
	unsigned char	*s;
	size_t			i;

	i = 0;
	d = (unsigned char *)dest;
	s = (unsigned char *)src;
	if (dest == src)
		return (dest);
	if (d > s)
		while (n--)
			d[n] = s[n];
	else
		while (n--)
		{
			d[i] = s[i];
			i++;
		}
	return (dest);
}
